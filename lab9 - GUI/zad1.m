% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
c_initial=str2double(get(handles.edit1,'String'));
c_final=str2double(get(handles.edit3,'String'));
ind = str2double(get(handles.edit2,'String'));
sliderValue = round(get(handles.slider1, 'Value'));
dx = (c_final-c_initial)/(sliderValue-1);
y= zeros(1,sliderValue);
x= zeros(1,sliderValue);
j=1;
for i= c_initial:dx:c_final
    y(j) = (1/(2 * pi * sqrt(ind * 10^(-6) * i * 10^(-12)))) * (10 ^(-6));
    x(j) = i;
    j = j + 1;
end
plot(x,y);
ylabel("Frequency [MHz]");
if get(handles.checkbox1,'Value')
grid on;
else
grid off;
end



